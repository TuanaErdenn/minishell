#include "libft.h"

int	ft_atoi(const char *str)
{
	int	j;
	int	z;

	j = 0;
	z = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			z = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		j = j * 10 + (*str - '0');
		str++;
	}
	return (j * z);
}
